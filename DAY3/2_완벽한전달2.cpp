#include <iostream>

// foo, goo 등 다양한 함수의 성능(수행시간)을 측정하고 싶다.

void foo(int a) {}
void goo(int& a) { a = 100; }


template<typename F>
void chronometry(F f, int arg)
{
	f(arg);	
}

int main()
{
	int n = 10;

	chronometry(foo, 10);
	chronometry(goo, n); 

	std::cout << n << std::endl;
}
